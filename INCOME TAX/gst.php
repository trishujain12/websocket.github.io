            <h3 class="txt2">Know the GSTIN - GST Identification Number</h3>
            <p class="txt3"> A 15-digit distinctive code that is provided to every taxpayer is the GSTIN. The GSTIN will be provided
                based on the state you live at and the PAN. Some of the main uses of GSTIN are mentioned below:</p>
            <ul>
                <li class="txt8"> Loans can be availed with the help of the number.</li>
                <li class="txt8">Refunds can be claimed with the GSTIN.</li>
                <li class="txt8">The verification process is easy with the help of the GSTIN.</li>
                <li class="txt8">Corrections can be made.</li>
            </ul>
        </div>
        <div class="tk">
            <h3 class="txt2">GST Certificate</h3>
            <p class="txt3">A GST Certificate is an official document that is issued by the concerned authorities for a business that
                has been enrolled under the GST system. Any business with an annual turnover of Rs.20 lakh or more and
                certain special businesses are required to be registered under this system. The GST registration
                certificate is issued in Form GST REG-06. If you are a registered taxpayer under this system, you can
                download the GST Certificate from the official GST Portal. The certificate is not issued physically. It
                is available in digital format only. GST Certificate contains GSTIN, Legal Name, Trade Name,
                Constitution of Business, Address, Date of liability, Period of Validity, Types of Registration,
                Particulars of Approving Authority, Signature, Details of the Approving GST officer, and Date of issue
                of a certificate.</p>
        </div>
        <div class="tk">
            <h3 class="txt2">GST Returns</h3>
            <p class="txt3">A <a href="https://www.bankbazaar.com/tax/gst-returns.html"> GST Returns </a> is a document that contains information about the income that a taxpayer must file with the
                authorities. This information used to compute the taxpayer's tax liability. Under the Goods and Services
                Tax, registered dealers must file their GST returns with details regarding their purchases, sales, input
                tax credit, and output GST. Businesses are expected to file 2 monthly returns as well as an annual
                return.</p>
        </div>
