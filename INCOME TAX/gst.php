            <h2 class="txt"><b>GST - Goods and Services Tax</b></h2>
            <div>
                <h3 class="txt2"><b>What is GST & How it Works?</b></h3>
                <p class="txt3">GST stands for Goods and Services Tax. It is an Indirect tax which introduced to replacing a host of
                    other Indirect taxes such as value added tax, service tax, purchase tax, excise duty, and so on. GST
                    levied on the supply of certain goods and services in India. It is one tax that is applicable all
                    over India.</p>
                <h3 class="txt2"><b>How GST Works in India?</b></h3>
                <ul>
                    <li class="txt6"><b>Manufacturer:</b> The manufacturer will have to pay GST on the raw material that is purchased
                        and the value that has been added to make the product.</li>
                    <li class="txt6"><b>Service Provider:</b> Here, the service provider will have to pay GST on the amount that is
                        paid for the product and the value that has been added to it. However, the tax that has been
                        paid by the manufacturer can be reduced from the overall GST that must be paid.</li>
                    <li class="txt6"><b>Retailer:</b> The retailer will need to pay GST on the product that has been purchased from
                        the distributor as well as the margin that has been added. However, the tax that has been paid
                        by the retailer can be reduced from the overall GST that must be paid.</li>
                    <li class="txt6"><b>Consumer:</b> GST must be paid on the product that has been purchased.</li>
                </ul>
            </div>
        </div>
        <div>
            <h3 class="txt2"><b>Tax Laws Before the Implementation of GST</b></h3>
            <p class="txt3"><b> * </b> The Centre and the State used to collect tax separately. Depending on the state, the tax
                regimes were
                different.
                <br><b> * </b> Even though import tax was levied on one individual, the burden was levied on another
                individual. <br> In the
                cases of direct tax, the taxpayer must pay the tax.
                <br><b> * </b> Prior to the introduction of GST, direct and indirect taxes were present in India.
            </p>
        </div>
        <div class="tk">
            <h3 class="txt2">Types of GST in India</h3>
            <p class="txt3">The four different types of GST are given below:</p>
            <ul>
                <li class="txt7"> Central Goods and Services Tax : CGST is charged on the intra state supply of products and
                    services.</li>
                <li class="txt7">State Goods and Services Tax : SGST, like CGST, is charged on the sale of products or services
                    within a state.</li>
                <li class="txt7">Integrated Goods and Services Tax : IGST is charged on inter-state transactions of products and
                    services.</li>
                <li class="txt7">Union Territory Goods and Services Tax : UTGST is levied on the supply of products and services in
                    any of the Union Territories in the country, viz. Andaman and Nicobar Islands, Daman and Diu, Dadra
                    and Nagar Haveli, Lakshadweep, and Chandigarh. UTGST is levied along with CGST.</li>
            </ul>
        </div>
        <div class="tk">
            <h3 class="txt2">Who Should Register for GST?</h3>
            <p class="txt3">The below mentioned entities and individuals must register for Goods And Services Tax:</p>
            <ul>
                <li class="txt8"> E-commerce aggregators</li>
                <li class="txt8">Individuals who supply through e-commerce aggregators</li>
                <li class="txt8">Individuals who pay tax as per the reverse change mechanism</li>
                <li class="txt8">Agents of input service distributors and suppliers</li>
                <li class="txt8">Non-Resident individuals who pay tax</li>
                <li class="txt8">Businesses that have a turnover that is more than the threshold limit</li>
                <li class="txt8">Individuals who have registered before the GST law was introduced</li>
            </ul>
        </div>
