            <h3 class="txt2">How do I Calculate GST?</h3>
            <p class="txt3">Calculating the amount that needs to be paid as GST when filing your returns can be quite tedious.
                Several aspects and factors must be taken into consideration, such as ITC, exempted supplies, reverse
                charge, etc. Failure to pay the entire GST amount can see you slapped with an 18% interest on the
                shortfall, thereby making it necessary to ensure that you pay the right amount towards GST.
           <br>
                The GST Calculator makes it simple for taxpayers to calculate the amount that needs to paid as GST. You
                will have to enter all the required details such as the month for which you are calculating GST, the due
                date for filing returns for the particular month, the actual date on which the returns are filed, the
                tax liability for the month, the purchases that attract Reverse Charge Mechanism, the opening balance of
                your cash ledger as well as your credit ledger and the eligible ITC.</p>
