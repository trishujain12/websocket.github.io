        <h2 class="txt2">Income Tax Deduction Section List</h2>

        <p class="txt3">Deductions for your taxable amount are available under various sections of the Income Tax Act, 1961.
            Deductions will have to be mentioned in the relevant ITR form at the time of e-filing income tax returns.
        </p>
        <ul>
            <li class="tp"><b>Section 80C :</b> Deductions under this section are only available to individuals and HUF. This section allows
                for certain investments like NSC, etc. and expenditures to be exempt from taxation up to the amount of
                Rs.1.5 lakh</li>
            <li class="tp"><b>Section 80CCC :</b> Deductions under this section are on payments made to LIC or any other approved insurance
                company under an approved pension plan. The pension policy must be up to Rs.1.5 lakh and be taken for
                the individual himself out of the taxable income.</li>
            <li class="tp"> <b>Section 80CCD :</b> Deductions under this section are for contributions to the New Pension Scheme by the
                assessee and the employer. The deduction is equal to the contribution, not exceeding 10% of his
                salary.The total deduction available under Section 80C, 80CCC and 80CCD is Rs.1.5 lakh. However,
                contributions to the Notified Pension Scheme under Section 80CCD are not considered in the Rs.1.5 lakh
                limit.</li>
            <li class="tp"><b>Section 80D :</b> This is the section that deals with income tax deductions on health insurance premiums paid.
                In the case of individuals, the insurance policy can be taken to cover himself, spouse, dependent
                children - for up to Rs.15,000 and parents (whether dependent or not) - for up to Rs.15,000. An
                additional deduction of Rs.5,000 is applicable if the insured is a senior citizen. In the case of HUF,
                any member can be insured, and the general deduction will be for up to Rs.15,000 and an additional
                deduction of Rs.5,000.A total of Rs.2.0 lakh can be claimed as deductions whether the assessee is an
                individual or a HUF.</li>
            <li class="tp"><b>Section 80DDB :</b> This section is for deductions on medical expenses that arise for treatment of a disease or
                ailment as specified in the rules (11DD) for the assessee, a family member or any member of a HUF.</li>
            <li class="tp"><b>Section 80E :</b> This section deals with the deductions that are applicable on the interest paid on education
                loans for an education in India.</li>
            <li class="tp"><b>Section 80EE :</b> This section deals with tax savings applicable to first time home-owners. Applies for
                individuals whose first home purchased has a value less than Rs.40 lakh and the loan taken for which is
                Rs.25 lakh or less.</li>
            <li class="tp"><b>Section 80RRB :</b> Deductions with respect to income by way of royalties or patents can be claimed under this
                section. Income tax can be saved on an amount up to Rs.3.0 lakh for patents registered under the Patents
                Act, 1970.</li>
            <li class="tp"><b>Section 80TTA :</b> This section deals with the tax savings that are applicable on interest earned in savings
                bank accounts, post office or co-operative societies. Individuals and HUFs can claim a deduction on an
                interest income of up to Rs.10,000.</li>
            <li class="tp"><b>Section 80U :</b> This section deals with the flat deduction on income tax that applies to disabled people,
                when they produce their disability certificate. Up to Rs.1.0 lakh can be non-taxed, depending on the
                severity of the disability.</li>
            <li class="tp"><b>Section 24 :</b> This section deals with the interest paid on housing loans that is exempt from taxation. An
                amount of up to Rs.2.0 lakh can be claimed as deductions per year, and is in addition to the deductions
                under Sections 80C, 80CCF and 80D. This is only for self-occupied properties. Properties that have been
                rented out, 30% of rent received and municipal taxes paid are eligible for tax exemption.</li>
        </ul>
