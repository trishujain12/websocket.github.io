            <h3 class="txt2">Penalty for not Paying Taxes</h3>
            <p class="txt3">The government can impose penalties of varying degrees on any individual or legal entity who evades taxes. The penalty is dependent on the category of the tax that has not been paid. This means that the amount that is owed as taxes should be paid and in addition to that, the fine as well as its interest is to be paid as the penalty.</p>
