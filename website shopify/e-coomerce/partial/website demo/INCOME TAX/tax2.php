      <h2 class="txt2">Benefits of Taxes</h2>
    <p class="txt3">The purpose of taxes is to provide the government with funds for spending without inflation. Taxes are used by the government for a variety of purposes, some of which are:</p>
            <ul class="txt4">
              <li>Funding of public infrastructure</li>
              <li>Development and welfare projects</li>
              <li>Defense expenditure</li>
              <li>Public insurance</li>
              <li>Scientific research</li>
              <li>Salaries of state and government employees</li>
              <li>Operation of the government</li>
              <li>Public transportation</li>
              <li>Unemployment benefits</li>
              <li>Pension schemes</li>
              <li>Law enforcement</li>
              <li>Public health</li>
              <li>Public education</li>
              <li>Public utilities such as water, energy, and waste management systems</li>
            </ul>
            <h3 class="txt2">Advantages of Paying Taxes</h3>
          <p class="txt3">It is compulsory and beneficial for anyone who earns a taxable salary (which is one that exceeds the basic exemption limit) to file their income tax returns. This is the case even if the tax liability is zero after deductions. However, even if your income is less than the basic exemption limit, there are advantages to filing taxes. Here are some of the benefits of paying your taxes on time:</p>
           <ul>
            <li class="txt6">Loan approvals:</li>
            <p class="txt7">When applying for a loan, especially home loans, vehicle loans, etc., major banks can request a copy of your income tax returns. This can be ITR from the last 2 to 3 years. Having ITR can even help to get a higher loan amount or to get your loan application reconsidered in case it was rejected at first. This is because banks calculate your ability to repay the loan based on your income. Income tax returns provide a clear picture of the income and the taxes that were paid on it in the previous years.</p>
            <li class="txt6">Visa applications: </li>
            <p class="txt7">Many foreign consulates require you to furnish your income tax returns of the previous years during the visa interview. While for some the most recent one will be sufficient, others require up to 2-3 years' worth of returns to be furnished. This is mandatory for the UK, US, Europe, and Canada, but not so much for South East Asian countries and the Middle East. This is because income tax returns are a proof that you are not trying to leave the country to evade taxes. Even when traveling abroad for leisure or business, it is always prudent to carry your ITR receipts as this will come in handy in the case of any emergency when you have to seek the help of a consulate.</p>
            <li class="txt6">Self-employed individuals: </li>
            <p class="txt7"> Freelancers, consultants, entrepreneurs, and partners of firms are not eligible for the Form 16. If their annual income exceeds the basic exemption limit, then ITR receipts can be furnished as proof of income. It is also proof of taxes paid. This will come in handy during any financial or business transaction.</p>
            <li class="txt6">Government tenders: </li>
            <p class="txt7">This depends on the individual government department with no specific strict rules, yet ITR receipts are sometimes requested to be furnished when applying for any government tenders. This is to ensure that you have sufficient income and can support the payment obligations.</p>
             <li class="txt6">Carrying forward of losses:</li>
             <p class="txt7">Short-term or long-term capital losses are usually carried forward to be adjusted against the capital gains made in the subsequent years. For example, the long-term capital loss of one year can be carried forward for up to 8 consecutive years that immediately succeed the year in which the loss had occurred. However, a long-term capital loss can be adjusted only against a short-term capital gain of that year. Short-term capital gains, however, can be adjusted against both short-term and long-term gains. However, this can only be availed if income tax returns have been filed.</p>
            <li class="txt6">Claiming tax refunds: </li>
            <p class="txt7"> Any refunds that are due from the IT Department can only be claimed if income tax returns have been filed. Even if income is below the tax exemption bracket, there could be refunds from different savings instruments that can be claimed if ITRs are filed. An example is fixed deposits, on which there is tax deducted at source at 10%.</p>
           <li class="txt6">High-cover life insurance:</li>
           <p class="txt7">Life cover or a Term Policy with sum insured that ranges from Rs.50 lakh to Rs.1 crore can be availed only by furnishing income tax returns which helps in the verification of annual income. Such a high insurance cover is only given when there is a high income for which income tax return receipts are necessary.</p>
         <li class="txt6">Compensation: </li>
         <p class="txt7"> For self-employed individuals, ITR receipts may have to be furnished in order to claim compensation in the event of a motor vehicle accident that results in a disability or accidental death. This is because, in order to arrive at the appropriate compensation, income of the person is to be established first.</p>
          </ul>
