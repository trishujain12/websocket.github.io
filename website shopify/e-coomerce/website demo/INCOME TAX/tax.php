            <h2 class="txt2">Income tax calculation</h2>
            <p class="txt3">Income tax calculation can be done either manually or by using an online income tax
                calculator. The amount of tax that must be paid will depend on the tax slab under which you fall. For
                salaried employees, income from salary includes the basic pay, House Rent Allowance (HRA), Transport
                Allowance, Special Allowance and any other allowances. However, certain components of your salary are
                tax-exempt, like Leave Travel Allowance (LTA), reimbursement of telephone bills, etc. In case HRA is
                part of your salary and you reside in a rented house, you are eligible to claim exemption. Apart from
                these exemptions, there is a standard deduction of up to Rs.50,000.</p>
        </div>
        <div class="txt">
            <h3 class="txt2">Income Tax Refund 2020-21</h3>
            <p class="txt3">In case you have paid more tax than your actual tax liability, you will be eligible to claim
                an income tax refund of the extra money you have paid. For example, if your TDS liability for FY 2019-20
                was Rs.35,000 and your employer deducted Rs.40,000 instead, you can claim a refund for the additional
                Rs.5,000 that was deducted. You can also claim an income tax refund in case you forgot to declare your
                tax-saving investments and tax has been charged to you without considering your deductions. Individuals
                can check income tax refund status on the official website of Income Tax Department.</p>
        </div>
        <div class="txt">
            <h2 class="txt2">Income Tax Saving Investments</h2>
            <p class="txt3">Declaring investments - From HRA, Life Insurance Premiums, National Savings Certificate,
                Leave Travel Allowance to Fixed Deposit (minimum of 5 years), ELSS Tax Saving Mutual Funds, and more, by
                ensuring that you have declared all your investments, you can achieve more deductions on tax. The
                following options can be considered for saving on income tax.</p>
            <div class="txt4">
                <h3 class="txt5"><b>Investment options</b></h3>
                <ul class="txt6">
                    <li>Mutual funds such as Equity Linked Savings Schemes (ELSS) can be claimed for tax deduction under
                        Section 80C. Compare to fixed deposits and PPF's, the ELSS offers a shorter lock-in period and
                        more benefits when it comes to making money</li>
                    <li>Unit Linked Insurance Plans (ULIP) are insurance schemes that are linked to the market. The
                        investment made under ULIP qualifies for tax deductions.</li>
                </ul>
            </div>
