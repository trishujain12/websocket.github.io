                        below 60 years of age, the tax exemption limit is Rs.2.5 lakh per annum. For individuals between
                        the age of 60 and 80, the tax exemption limit is Rs.3 lakh. For individuals above the age of 80,
                        the tax exemption limit is Rs.5 lakh. There are different tax slabs for different income
                        amounts.Apart from individuals, legal entities are also liable to pay taxes. These include all
                        Artificial Judicial Persons, Hindu Undivided Family (HUF), Body of Individuals (BOI),
                        Association of Persons (AOP), companies, local firms, and local authorities.</p>
                    <li class=txt7>2.Capital gains:</li>
                    <p class="txt8"> <a href="https://www.bankbazaar.com/tax/capital-gains-tax.html">Capital gains</a> tax is levied
                        on the sale of a property or money received through an investment. It could be from either
                        short-term or long-term capital gains from an investment. This includes all exchanges made in
                        kind that is weighed against its value.</p>
                    <li class=txt7>3. Securities transaction Tax:</li>
                    <p class="txt8"> <a href="https://www.bankbazaar.com/tax/securities-transaction-tax.html">STT</a> is levied on
                        stock market and securities trading. The tax is levied on the price of the share as well as
                        securities traded on the ISE (Indian Stock Exchange).</p>
                    <li class=txt7>4. Prerequisite Tax: </li>
                    <p class="txt8">These are taxes that are levied on the different benefits and perks that are provided by a
                        company to its employees. The purpose of the benefits and perks, whether it is official or
                        personal, is to be defined.</p>
                    <li class=txt7>5. Corporate tax: </li>
                    <p class="txt8">The income tax paid by a company is defined as the corporate tax. It is based on the different
                        slabs that the revenue falls under. The sub-categories of corporate taxes are as follows:</p>
                    <ul>
                        <li class="tt">5.1. Dividend distribution tax <a
                                href="https://www.bankbazaar.com/tax/dividend-distribution-tax.html">( DDT)</a>: </li>
                        <p class="tk">This tax is levied on the dividends that companies pay to the investors. It applies to the
                            net or gross income that an investor receives from the investment.</p>
                        <li class="tt">5.2. Fringe benefit tax <a
                                href="https://www.bankbazaar.com/tax/fringe-benefit-tax.html">(FBT)</a>:</li>
                        <p class="tk">This is tax levied on the fringe benefits that an employee receives from the company. This
                            include expenses related to accommodation, transportation, leave travel allowance,
                            entertainment, retirement fund contribution by the employee, employee welfare, Employee
                            Stock Ownership Plan (ESOP), etc.</p>
                        <li class="tt">5.3. Minimum Alternative tax <a
                                href="https://www.bankbazaar.com/tax/minimum-alternate-tax.html"> (MAT)</a>: </li>
                        <p class="tk">Companies pay the IT Department through MAT which is governed by Section 115JA of the IT Act.
                            Companies that are exempt from MAT are those that are in the power and infrastructure
                            sectors.</p>
