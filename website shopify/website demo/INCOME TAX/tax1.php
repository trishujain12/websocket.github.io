                <h4 class="txt5">Sub-categories of Direct Taxes :</h4>
                <p class="txt6">The following are the sub-categories of direct taxes:</p>
                <ul>
                    <li class=txt7>1.Income tax:</li>
                    <p class="txt8">This is the tax that is levied on the annual income or the profits which is directly paid to the
