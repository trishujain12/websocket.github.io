                <h2 class="txt2">Penalty for Late Filing TDS Return</h2>
                <p class="txt3">Here are the penalties levied by the Income Tax Department for the failure to submit or
                    defaults in submitting your TDS return/statements:</p>
                <ul>
                    <li class="tp2"><b>Failure to submit your returns:</b> Under Section 272A (2) of the Income Tax Act,
                        a penalty of Rs.100 will be levied for each day that the returns remain unsubmitted, subject to
                        a maximum of the TDS amount.</li>
                    <li class="tp2"><b>Failure to file your returns on time:</b> Under Section 234E of the Income Tax
                        Act, a penalty of Rs.200 will be levied for each day that the returns remain unfiled, subject to
                        a maximum of the TDS amount.</li>
                    <li class="tp2"><b>For defaults in the filing of TDS statement:</b> Under Section 271H of the Income
                        Tax Act, a penalty of Rs.10,000 to Rs.1 lakh will be levied in case the deductor defaults at the
                        time of filing TDS return within the due date.</li>
                    <li class="tp2"><b>For incorrect details:</b> Under Section 271H of the Income Tax Act, a penalty of
                        Rs.10,000 to Rs.1 lakh will be charged in case the deductor submits incorrect information
                        pertaining to PAN, challan particulars, TDS amount, etc</li>
                    <li class="tp2"><b>For non-payment of TDS: </b> Under Section 201A of the Income Tax Act, interest
                        will also be levied along with the penalty in case TDS is not paid within the due date. In case
                        a part of the tax amount or the whole of it is not deducted at source, interest will be charged
                        at 1.5% every month starting from the date on which the tax was deductible to the date on which
                        the tax is actually deducted.</li>
                </ul>
