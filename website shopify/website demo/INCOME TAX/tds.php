                <h2 class="txt2">What is TDS?</h2>
                <p class="txt3">TDS or Tax Deducted at Source is a specific amount that is reduced when a certain
                    payment like salary,
                    commission, rent, interest, professional fees, etc. is made. The person who makes the payment
                    deducts
                    tax at the source, while the person who receives a payment/income has the liability to pay tax. It
                    lowers tax evasion because the tax will be collected at the time of making a payment.
                    <br>
                    <br>Read about the Union Budget 2021 Highlights:
                    <br>Dividend payments to REITs and InvITs will be exempt from TDS
                    <br>In the Union Budget for FY22, Finance Minister Nirmala Sitharaman announced that dividend
                    payments
                    to REITs (Real Estate Investment Trusts) and InvITs (Infrastructure Investment Trusts) will be made
                    exempt from Tax Deduction at Source (TDS). This aims to increase compliance with tax laws. A
                    proposal
                    was also made to take advance tax liability on dividend income after the payment or declaration of a
                    dividend has been made.
                </p>
