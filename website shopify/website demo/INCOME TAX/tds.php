                <h2 class="txt2">What is a TDS Certificate?</h2>
                <p class="txt3">TDS Certificates are of two types: Form 16 and Form 16A. Under Section 203 of the Income
                    Tax Act, 1961, a certificate must be provided to the deductee showing the amount that has been
                    subtracted as tax. The deductor is liable to provide this form to the deductee.</p>
                <ul>
                    <li class="tp2"><b>For salaried class: </b> In case of salaried employees, employers are required to
                        provide them with Form 16 with a mention of the amount that has been deducted as TDS. Form 16
                        contains a host of details such as the computation of tax, the deduction of tax, and the payment
                        of TDS. Employers must issue this form to their employees before May 31 of the following
                        financial year.</li>
                    <li class="tp2"><b>For non-salaried class:</b> The deductor provides the deductee with Form 16A, and
                        it contains all the details regarding the computation of tax, the deduction of TDS, and
                        payments.</li>
                </ul>
